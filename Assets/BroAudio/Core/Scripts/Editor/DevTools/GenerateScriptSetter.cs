    public interface IAudioSourceModifierSample
    {
        float volume { set; }
    }

    public class AudioSourceModifierSample : BroModifier, IAudioSourceModifierSample
    {
        private AudioSource _audioSource;

        public const float Default_Volume = 1f;
        private bool _hasVolumeResetAction = false;
        public float volume
        {
            set
            {
                if (!_hasVolumeResetAction)
                {
                    AddResetAction(() => _audioSource.volume = Default_Volume);
                    _hasVolumeResetAction = true;
                }

                _audioSource.volume = value;
            }
        }
    }
